About us:



HMS is the only JCI Accredited 450-bed multi-disciplinary super-specialty tertiary care hospital in Bangladesh, confidently providing comprehensive health care with the latest medical, surgical and diagnostic facilities. These services are provided by expert medical professionals, skilled nurses and technologists using state-of-the-art technology. The hospital started its operation on April 16, 2005 with a mission to provide high quality international standard healthcare which will meet the needs and exceed the expectations of the people of Bangladesh. It is an eleven-storied building built on four acres of land with 435,000 square feet floor space, which was constructed with assistance of renowned architecture company Smith Group, USA. The hospital is a joint project of Apollo Hospitals Enterprise Limited and STS Holdings Limited (STS Group).

HMS has all the characteristics of a world-class hospital with wide range of services and specialists, equipments and technology, ambience and service quality. The hospital is a showcase of synergy of medical technology and advances in IT through paperless medical records. The skilled nurses, technologists and administrators of HMS, aided by state-of-the-art equipments, provide a congenial infrastructure for the medical professionals in providing healthcare of international standards.

Since its inception to date, approximately 354,720 patients have registered with HMS and over 1,157,039 patients have availed out-patient consultation across over 29 disciplines. The hospital is the first and only in Bangladesh to get accredited by Joint Commission International (JCI), which reassures its continued commitment to the highest quality international standard of healthcare in the region. We are not only about cutting-edge technology and performance-driven skill of our doctors and nurses, but we are also about hope, as symbolized by the flames of hope in the torch on our logo.

WHAT WE HAVE

Dedicated to providing multidisciplinary medical care and backed by state-of-the-art facilities, HMS offers team-based quality patient care that is widely acknowledged to be amongst the best in Bangladesh. At HMS, patients have ready access to more than 29 specialized departments and services, which are established as national referral centres. We have five Centres of Excellence and five Strategic Business Units. Modern lithotripsy service, i.e. the treatment of liver and ureter stones without surgery, is provided by the hospital using extracorporeal shock wave. The hospital also offers a complete Dialysis Unit for the treatment of patients with liver disorders. The hospital has 1.5 Tesla MRI, 64 Slice CT Scan, Color Doppler, Ultrasound, and Gamma Camera, which have created the most modern Radiology and Nuclear Medicine  service in the country, with equipment that is faster and capable of conducting a wide range of examinations with a higher degree of detail. This is in addition to EEG, EMG, neurophysiology, ECG, stress test, pulmonary function test, etc.  Our international standard Lab Medicine conducts External Quality Control Program with Bio-Rad, USA and offers a variety of modern tests and diagnostic tools, some of which are not available anywhere else in Bangladesh, with regular additions. 

    
